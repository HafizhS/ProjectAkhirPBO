    public void insertToPengembalian() {
        Date date = new Date();
        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
        String tanggal = sdf.format(date);
        
        Date waktu = new Date();
        SimpleDateFormat format = new SimpleDateFormat("HH:mm:dd");
        String now = format.format(waktu);
        
        Random random = new Random();
        for (int i = 1; i <= 1; i++) {
            id_pengembalian = 1+random.nextInt(100000);
        } 
        try{
            String sql = "INSERT INTO tbl_pengembalian VALUES ('"+id_pengembalian+"','"+nis+"','"+tanggal+"'"
                    + ", '"+now+"')";
            Statement stmt = koneksi.createStatement();
            System.out.println(sql);
            stmt.executeUpdate(sql);
        }catch(SQLException ex) {
            ex.printStackTrace();
        }
    }
