    int nis;
    public void loadData() {
        try{
            String sql = "SELECT * FROM tbl_peminjaman,tbl_kelas,tbl_murid"
                    + " WHERE (tbl_peminjaman.nis = tbl_murid.nis) AND (tbl_murid.id_kelas = tbl_kelas.id_kelas) AND"
                    + " (tbl_peminjaman.id_peminjaman = '"+idPeminjaman+"')";
            Statement stmt = koneksi.createStatement();
            System.out.println(sql);
            ResultSet rs = stmt.executeQuery(sql);
            rs.first();
            txtNama.setText(rs.getString("nama"));
            txtKelas.setText(rs.getString("nama_kelas"));
            cmbStatus.setSelectedIndex(rs.getInt("status"));
            nis = rs.getInt("tbl_peminjaman.nis");
        }catch(SQLException ex) {
            ex.printStackTrace();
        }
    }
    DefaultTableModel dtm;
    int no;
    public void loadDataDetail() {
        String[] kolom = {"NO","ID Barcode","Nama Barang"};
        dtm = new DefaultTableModel(null,kolom);
        try{
            String sql = "SELECT * FROM tbl_detail_peminjaman,tbl_peminjaman,tbl_barang "
                    + "WHERE (tbl_detail_peminjaman.id_peminjaman = tbl_peminjaman.id_peminjaman) "
                    + "AND (tbl_detail_peminjaman.id_barang = tbl_barang.id_barcode) "
                    + "AND (tbl_detail_peminjaman.id_peminjaman = '"+idPeminjaman+"')";
            Statement stmt = koneksi.createStatement();
            ResultSet rs = stmt.executeQuery(sql);
            while(rs.next()) {
                dtm.addRow(new String[] {
                    no+"",rs.getString("tbl_detail_peminjaman.id_barang"),rs.getString("tbl_barang.nama_barang")
                });
                no++;
            }
        }catch(SQLException ex) {
            ex.printStackTrace();
        }
        tbl_pinjam.setModel(dtm);
    }
